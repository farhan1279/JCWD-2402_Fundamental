// Comparison Operator / Operator Pembanding
// Comparison operator akan menghasilkan value berupa boolean 
// >, >=, <, <=, !=, ==, ===

console.log(10 > 9)
let angka1 = 9
let angka2 = 100
console.log(angka1 < angka2)
console.log(10 >= 10)
console.log(10 <= 10)
console.log(9 != 10)

// == Membandingkan value nya saja
console.log(9 == 9) // true
console.log(9 == '9') // true

// === Membandingkan value dan juga tipe datanya
console.log(10 === '10') // false
console.log(false === 'false')



console.log(9 != 10 )